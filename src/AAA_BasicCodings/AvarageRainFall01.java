package AAA_BasicCodings;

public class AvarageRainFall01 {
	public static void main ( String[] args )
	{
       double April, May, June, AvrgRainfall;
       April =  1 ;
       May =  2 ;
       June =  3 ;
       AvrgRainfall = (April + May + June ) / 3 ;
        
        System.out.println("Rainfall for April:"+'\t'+ April);
        System.out.println("Rainfall for May:"+'\t'+ May);
        System.out.println("Rainfall for June:"+'\t'+ June);
        System.out.println("Average Rainfall:"+'\t'+ AvrgRainfall);

	}
}
