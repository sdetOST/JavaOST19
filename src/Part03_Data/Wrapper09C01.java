package Part03_Data;

public class Wrapper09C01 {
	
	public static void main ( String[] args ) {
		
		Integer x = new Integer( 103 );    // hold the value 103 
	                                           // inside an Integer object
	    Double y = new Double( -32.78 );  // hold a double precision 
	                                           // value inside a Double object
	    int a = 21;
	    double b = 12.34;
	    
	    System.out.println( "Integer object holds: x = " + x );
	    System.out.println( "Double  object holds: y = " + y );
	    System.out.println( "int primative data holds: a = " + a );
	    System.out.println( "double primative data holds: b =  " + b );
	  }
}
