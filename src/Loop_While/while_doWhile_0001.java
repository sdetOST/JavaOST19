package Loop_While;

public class while_doWhile_0001 {

	public static void main(String[] args) {
		
		int a=1;
		while(a <=3 ) {
			System.out.print(a+", ");
			a++;
		}
		
System.out.println("\n-----------------------");
		
		int b = 1;
		do {
			System.out.print(b+", ");
			b++;
		}while(b <= 3 );
		
System.out.println("\n-----------------------");
		
		int c = 1;
		while(c <= 3 ) {
			System.out.print(c+++", ");
		}
		
System.out.println("\n-----------------------");
		
		int d = 1;
		while(d++ <= 3 ) {
			System.out.print(d+", ");
		}
		
System.out.println("\n-----------------------");
		
		int e = 1;
		do {
			System.out.print(e+++", ");
		}while(e <= 3);
		
System.out.println("\n-----------------------");
		
		int f = 1;
		do {
			System.out.print(f+", ");
		}while(++f <= 3);
		
System.out.println("\n-----------------------");

		int g= 3;
		while(g >= 1 ) {
			System.out.print(g+", ");
			g--;
		}

System.out.println("\n-----------------------");

		int h = 3;
		do {
			System.out.print(h--+", ");
		}while(h >= 1);

System.out.println("\n-----------------------");


	}	

}
